export const curriculumData: SubjectCurriculum[] = [
  ...baseCurriculumData,
  ...computingScienceData,
  ...m3CurriculumData
];

export const getAllSubjects = () => {
  const subjects = new Set<string>();
  curriculumData.forEach(item => subjects.add(item.subject));
  return Array.from(subjects);
};

export const getGradeLevels = (subject: string) => {
  const grades = new Set<string>();
  curriculumData.filter(item => item.subject === subject).forEach(item => grades.add(item.gradeLevel));
  return Array.from(grades);
};

export const getStandards = (subject: string, gradeLevel: string) => {
  const item = curriculumData.find(item => item.subject === subject && item.gradeLevel === gradeLevel);
  return item ? item.standards : [];
};
